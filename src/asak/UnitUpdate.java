    boolean updateValue(int unit) throws SQLException{
        
        if (checkValid(unit)){
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/ASAKDatabase","asakadmin","asakadmin");
            Statement st = con.createStatement();
            int i = st.executeUpdate("Update costumer_details set current_unit="+unit+" where meter_number="+meter+"");
            System.out.println(i);
            if (i<=0)
                    JOptionPane.showMessageDialog(this, "Failed");
            return i>0;
        }
        return false;
    }
    
    
