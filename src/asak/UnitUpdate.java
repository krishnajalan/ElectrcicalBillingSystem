    boolean checkValid(int unit) throws SQLException{
        Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/ASAKDatabase","asakadmin","asakadmin");
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("Select * from costumer_details where meter_number="+meter);
        if (rs.next()){
            if ( rs.getInt("units_consumed")<unit )
                    return true;
        }
        JOptionPane.showMessageDialog(this, "Invalid unit");
        return false;
    }
